/*6. Standard Output
Write a program that prints various data types (such as Int, String, Double) using println and illustrates
the difference in handling these types during printing.*/

fun main() {
    val number: Int = 10
    println("The number is $number")

    val text: String = "Hello Kotlin!"
    println("The text is $text")

    val decimal: Double = 3.14
    println("The decimal is $decimal")

}